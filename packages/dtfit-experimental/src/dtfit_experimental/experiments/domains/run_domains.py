"""Run the per-domain validation suite and refresh its index.

    python -m dtfit_experimental.experiments.domains.run_domains          # full (parallel)
    python -m dtfit_experimental.experiments.domains.run_domains --quick  # fast smoke run
    python -m dtfit_experimental.experiments.domains.run_domains --jobs 2 # cap workers
    python -m dtfit_experimental.experiments.domains.run_domains --jobs 1 # serial (debug)

Where ``cases`` evaluates each EAC/LSI adaptation in isolation, this suite is a
**comprehensive per-application-domain study**: each domain tests every applicable
dtfit method against the **established domain-standard methods**, on synthetic
*and real* data. The shared driver
(:mod:`dtfit_experimental.experiments._runner`) runs the domains in parallel and
regenerates ``DOMAINS.md``.
"""

from __future__ import annotations

from pathlib import Path

from dtfit_experimental.experiments._runner import Suite, Result, main

HERE = Path(__file__).resolve().parent
PACKAGE = "dtfit_experimental.experiments.domains"

# (name, title, methods-and-baselines blurb for the index table)
DOMAINS = [
    ("forecasting", "Forecasting — comprehensive cross-method study",
     "dtfit LSI, EAC, #2 Fourier-LSI, #5 boosting, auto-merged pipeline — on 12 "
     "series × 2 horizons (8 measured datasets + 4 physics/signal waveforms: RLC "
     "transient, AC+harmonics, AM, chirp), with the structurally-correct model per "
     "series, vs random walk, seasonal-naïve, drift, poly-extrap, Holt-Winters "
     "ETS, Theta, (S)ARIMA, MLP, LSTM"),
    ("parameter_estimation", "Model parameter estimation — comprehensive",
     "dtfit LSI, EAC, #6 adaptive-EAC, #3 ensemble, #4 joint, merged selector — on "
     "15 nonlinear model families (oscillatory/exp/multi-exp/peak/sigmoid/"
     "rational-saturating/power) × an applicability map, noise & outlier sweeps, "
     "sparse/transient/short-record/multi-channel regimes, real COVID/FX recovery, "
     "vs NLLS, robust NLLS, MLP, Gaussian process"),
    ("big_data", "Big-data processing — batch, streaming & distributed",
     "dtfit GEMM batch (fit_lsi_batched), fused streaming PartitionedBatchLSI, "
     "distributed merge (#1), streaming EACFilter — 4 multi-channel panels "
     "+ real 321-channel electricity; exactness, GB-scale memory wall, numerical "
     "stability (float32/float64/Kahan), mergeability, O(1)/sample online cost; vs "
     "per-channel NLLS, vectorised polynomial lstsq, sklearn SGD partial_fit, RLS"),
    ("embedded_control", "Embedded real-time control — comprehensive online ID",
     "dtfit EACFilter, LSIFilter, FilterBank + fused χ² "
     "detector + inflate — 4 plant shapes + applicability map, robustness profile "
     "(Gaussian noise / outliers / dropout), multi-axis fault detection, "
     "deployable footprint, real FX streaming; vs EKF, RLS, constant-accel "
     "Kalman, sliding-window refit"),
]


def _extra_sections(results: list[Result]) -> list[str]:
    lines = ["\n## dtfit methods tested vs baselines, per domain\n",
             "| domain | dtfit methods + established baselines |", "|---|---|"]
    for _name, title, merged in DOMAINS:
        lines.append(f"| {title.split(' — ')[0]} | {merged} |")
    lines += [
        "\n## Relationship to the `cases` suite\n",
        "The cases isolate one optimization/adaptation per folder; these domains "
        "exercise them **together and against the methods a practitioner in that "
        "domain actually uses**. Every report keeps the cases' honest-negative "
        "tone: dtfit trails the classical toolkit on near-RW / irregular series, "
        "the integral methods miss weakly-identifiable parameters (Michaelis–"
        "Menten), the area filter is the wrong measurement for oscillations (use "
        "the spectrum filter), robustness wants a dedicated robust loss over "
        "ensembling, streaming trades throughput for bounded memory, and online "
        "detection is SNR-limited.\n",
        "\n*This index is regenerated on every `run_domains.py` run.*"]
    return lines


SUITE = Suite(
    package=PACKAGE,
    experiments=[(name, title) for name, title, _ in DOMAINS],
    index_path=HERE / "DOMAINS.md",
    index_title="dtfit domain validation suite — index",
    intro=["Generated by `run_domains.py`. Where the `cases` suite evaluates each "
           "EAC/LSI adaptation in isolation, this suite is a **comprehensive "
           "per-application-domain study**: each domain tests every applicable "
           "dtfit method against the **established domain-standard methods**, on "
           "synthetic *and real* data, checking validity, applicability and "
           "usefulness. Each report opens with a detailed *Methods under test* and "
           "*Baseline methods* section.\n"],
    noun="domain",
    numbered=False,
    extra_sections=_extra_sections,
)


if __name__ == "__main__":
    main(SUITE)

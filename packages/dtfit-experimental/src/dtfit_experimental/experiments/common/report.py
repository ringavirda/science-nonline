"""ReportWriter -- accumulate an experiment write-up and emit its report.md.

Each experiment owns a folder; the report lands at ``<folder>/report.md`` and
its figures under ``<folder>/figures/``. The writer keeps the
intent/setup/results narrative and the embedded figures in one place so every
experiment produces a consistent, self-contained markdown report.
"""

from __future__ import annotations

from datetime import date
from pathlib import Path

from .metrics import md_table
from .plotting import savefig

EXPERIMENTS_DIR = Path(__file__).resolve().parent.parent


class ReportWriter:
    def __init__(self, exp_dir: Path | str, title: str, intent: str = "") -> None:
        self.exp_dir = Path(exp_dir)
        self.fig_dir = self.exp_dir / "figures"
        self.fig_dir.mkdir(parents=True, exist_ok=True)
        self.title = title
        self._parts: list[str] = [f"# {title}\n"]
        self._parts.append(f"*Generated by `{self.exp_dir.name}/run.py` on "
                           f"{date.today().isoformat()}.*\n")
        if intent:
            self._parts.append(f"## Intent\n\n{intent}\n")

    def section(self, heading: str, body: str = "", level: int = 2) -> "ReportWriter":
        self._parts.append(f"{'#' * level} {heading}\n")
        if body:
            self._parts.append(body.rstrip() + "\n")
        return self

    def text(self, body: str) -> "ReportWriter":
        self._parts.append(body.rstrip() + "\n")
        return self

    def table(self, headers: list[str], rows: list[list], caption: str = "") -> "ReportWriter":
        if caption:
            self._parts.append(f"*{caption}*\n")
        self._parts.append(md_table(headers, rows) + "\n")
        return self

    def figure(self, fig, name: str, caption: str = "") -> "ReportWriter":
        """Save ``fig`` into ``figures/<name>.png`` and embed it."""
        if not name.endswith(".png"):
            name += ".png"
        savefig(fig, self.fig_dir / name)
        cap = caption or name
        self._parts.append(f"![{cap}](figures/{name})\n")
        if caption:
            self._parts.append(f"*{caption}*\n")
        return self

    def write(self) -> Path:
        path = self.exp_dir / "report.md"
        path.write_text("\n".join(self._parts), encoding="utf-8")
        return path
